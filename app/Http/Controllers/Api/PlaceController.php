    public function nearest($id)
    {
        // return "Sajal";
        $place = Place::findOrfail($id);
        $minlat = $place->lat - 0.15;
        $maxlat = $place->lat + 0.15;
        $minlng = $place->lng - 0.15;
        $maxlng = $place->lng + 0.15;

        

        $query = Place::whereBetween('lat',[$minlat,$maxlat])->whereBetween('lng',[$minlng,$maxlng])->get();
        return response()->json($query);
    }

